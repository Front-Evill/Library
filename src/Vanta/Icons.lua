local function ResolveIcon(name)
	if not name or name == "" then
		return ""
	end

	if typeof(name) == "number" then
		return "rbxassetid://" .. tostring(name)
	end

	if name:sub(1, 12) == "rbxassetid:/" then
		return name
	end

	if name:match("^%d+$") then
		return "rbxassetid://" .. name
	end

	local urlId = name:match("[?&]id=(%d+)") or name:match("/(%d+)/?$")
	if urlId and (name:find("roblox.com") or name:find("rbxassetid")) then
		return "rbxassetid://" .. urlId
	end

	local key = name
	if key:sub(1, 11) ~= "FrontEvill-" then
		key = "FrontEvill-" .. key
	end

	return Icons[key] or Icons["FrontEvill-circle"]
end

return {
	Icons = Icons,
	ResolveIcon = ResolveIcon,
}
